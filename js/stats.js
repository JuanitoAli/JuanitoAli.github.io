function getMinDistance(latCenter, lngCenter, listLocations) {
    var min = getDistance(latCenter, lngCenter, listLocations[0][1], listLocations[0][2]);
    var d;
    var ans = {latitude: listLocations[0][1], longitude: listLocations[0][1] };

    for(var i = 1; i < listLocations.length; ++i) {
        d = getDistance(latCenter, lngCenter, listLocations[i][1], listLocations[0][2]);
        if(d < min)
            ans = {latitude: listLocations[i][1], longitude: listLocations[i][2]}
    }
    return ans;
}

